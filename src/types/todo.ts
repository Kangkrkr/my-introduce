/**
 * 할 일(Todo) 객체의 구조를 정의하는 TypeScript 인터페이스입니다.
 * 이 인터페이스는 애플리케이션 전반에서 할 일 데이터를 다룰 때 일관된 타입을 보장합니다.
 */
export interface Todo {
  /**
   * 할 일의 고유 식별자입니다.
   * 각 할 일을 구분하는 데 사용됩니다.
   */
  id: number
  /**
   * 할 일의 내용 또는 제목입니다.
   * 사용자가 수행해야 할 작업을 설명하는 텍스트입니다.
   */
  title: string
  /**
   * 할 일의 완료 상태를 나타내는 불리언 값입니다.
   * true이면 할 일이 완료되었음을, false이면 아직 완료되지 않았음을 의미합니다.
   */
  completed: boolean
}